	private static final String FORMAT = "http://textures.minecraft.net/texture/";

	public static String getTexture(String texture) {
		if(texture.matches("[0-9a-f]+")) {
			return getTextureFromId(texture);
		} else if(texture.matches("[a-zA-Z0-9+/]+={0,2}")) {
			return texture;
		} else {
			return null;
		}
	}


	public static String getTextureFromId(String id) {
		return FORMAT + id;
	}

