	private static final String FORMAT = "{\"textures\":{\"SKIN\":{\"url\":\"http://textures.minecraft.net/texture/ID\"}}}";

	public static String getTexture(String texture) {
		if(texture.matches("[0-9a-f]+")) {
			return getTextureFromId(texture);
		} else if(texture.matches("[a-zA-Z0-9+/]+={0,2}")) {
			return texture;
		} else {
			return null;
		}
	}


	public static String getTextureFromId(String id) {
		return Base64.getEncoder().encodeToString(FORMAT.replace("ID", id).getBytes());
	}

